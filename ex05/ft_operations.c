int	add(int a, int b)
{
	return (a + b);
}

int	substitute(int a, int b)
{
	return (a - b);
}

int	multiple(int a, int b)
{
	return (a * b);
}

int	divide(int a, int b)
{
	return (a / b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
